# Example 11: NumPy - Indexing with Negative Indices
import numpy as np

# Creating a 1D array
array_1d = np.array([2,4,8,10,12])
last_element=array_1d[-1]
second_last_element=array_1d[-2]
third_last_element=array_1d[-3]
print("Last element :", last_element)
print("Second  last element :", second_last_element)
print("Third last element :", third_last_element)
